class Solution(object):
    def treeToDoublyList(self, root):
        """
        :type root: Node
        :rtype: Node
        """

        def inorder(root):
            if not root:
                return

            inorder(root.left)
            result.append(root)
            inorder(root.right)

        if not root:
            return

        result = []
        inorder(root)

        for i in range(len(result) - 1):
            result[i].right = result[i + 1]
            result[i + 1].left = result[i]

        result[0].left = result[-1]
        result[-1].right = result[0]

        return result[0]


