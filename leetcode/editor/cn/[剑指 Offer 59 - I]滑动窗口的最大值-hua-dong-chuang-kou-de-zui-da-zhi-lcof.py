class Solution(object):
    def maxSlidingWindow(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: List[int]
        """
        if not nums or k <= 0 or k > len(nums):
            return []

        result, m, n = [], float('-INF'), len(nums)

        for i in range(0, n - k + 1):
            result.append(max(nums[i:i + k]))

        return result


